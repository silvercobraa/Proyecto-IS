package proyectois;

import java.util.Date;

public class Periodo {

    private Date comienzo;
    private Date fin;

    public Periodo() {

    }

    public Date getComienzo() {
        return comienzo;
    }

    public void setComienzo(Date comienzo) {
        this.comienzo = comienzo;
    }

    public Date getFin() {
        return fin;
    }

    public void setFin(Date fin) {
        this.fin = fin;
    }
    
}
